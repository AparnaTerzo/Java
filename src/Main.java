        int round = Math.round(2.8f);
        System.out.println("Rounded value = "+round);

        //ceil
        int ceil = (int)Math.ceil(2.2f);
        System.out.println("Ceiled value = "+ceil);

        //floor
        int floor = (int)Math.floor(2.8f);
        System.out.println("Floor value = "+floor);

        //max
        int max = Math.max(9,78);
        System.out.println("Max value = "+max);

        //min
        int min = Math.min(9,78);
        System.out.println("Min value = "+min);

        //max
        int random = (int) Math.round(Math.random()*100);
